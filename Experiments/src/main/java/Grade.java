    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
                nam = name.getText();
                grad1 = Double.parseDouble(grade1.getText());
                grad2 = Double.parseDouble(grade2.getText());
                grad3 = Double.parseDouble(grade3.getText());
                avg = (grad1 + grad2 + grad3)/3;
                txtArea.setText("Average : " + avg);
                if(avg > 90)
                {
                    g = "A";
                    txtArea.setText("Grade : " + g);
                }
                else if(avg > 80)
                {
                    g = "B";
                    txtArea.setText("Grade : " + g);
                }
                else if(avg > 70)
                {
                    g = "C";
                    txtArea.setText("Grade : " + g);
                }
                else if(avg > 60)
                {
                    g = "D";
                    txtArea.setText("Grade : " + g);
                }
                else
                {
                    g = "F";
                    txtArea.setText("Grade : " + g);
                }
    }//GEN-LAST:event_jButton2ActionPerformed

