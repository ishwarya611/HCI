    private void btneqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneqActionPerformed
        // TODO add your handling code here:
        num2 = Double.parseDouble(txtres.getText());
        if(op=="+")
        {
            res = num1+num2;
            txtres.setText(String.valueOf(res));
        }
        else if(op=="-")
        {
            res = num1-num2;
            txtres.setText(String.valueOf(res));
        }
        else if(op=="*")
        {
            res = num1*num2;
            txtres.setText(String.valueOf(res));
        }
        else if(op=="/")
        {
            res = num1/num2;
            txtres.setText(String.valueOf(res));
        }
    }//GEN-LAST:event_btneqActionPerformed

